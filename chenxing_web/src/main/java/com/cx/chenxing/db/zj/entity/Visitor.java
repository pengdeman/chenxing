package com.cx.chenxing.db.zj.entity;

public class Visitor {
	private Integer id;
	private Integer visitorId;
    private String visitorName;
    private Integer bvisitorId;
    private String bvisitorName;
    private String visitorAdr;
    private String visitorTime;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getVisitorId() {
		return visitorId;
	}
	public void setVisitorId(Integer visitorId) {
		this.visitorId = visitorId;
	}
	public String getVisitorName() {
		return visitorName;
	}
	public void setVisitorName(String visitorName) {
		this.visitorName = visitorName;
	}
	public Integer getBvisitorId() {
		return bvisitorId;
	}
	public void setBvisitorId(Integer bvisitorId) {
		this.bvisitorId = bvisitorId;
	}
	public String getBvisitorName() {
		return bvisitorName;
	}
	public void setBvisitorName(String bvisitorName) {
		this.bvisitorName = bvisitorName;
	}
	public String getVisitorAdr() {
		return visitorAdr;
	}
	public void setVisitorAdr(String visitorAdr) {
		this.visitorAdr = visitorAdr;
	}
	public String getVisitorTime() {
		return visitorTime;
	}
	public void setVisitorTime(String visitorTime) {
		this.visitorTime = visitorTime;
	}
}
